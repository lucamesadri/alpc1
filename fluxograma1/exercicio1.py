# 1)Faça um algoritmo que solicite o número de horas que um trabalhador realiza por dia, 
#   solicite o número de dias trabalhados em um mês e
#   apresente o número total de horas trabalhadas no mês.

print("Digite horas trabalhadas por dia")
horasDia = int(input())

print("Digite dias trabalhadas por mês")
diasMes = int(input())

result = horasDia * diasMes
print("O número de horas por mês é:", result)